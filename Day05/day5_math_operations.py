print("*==============================*")
print("|         CALCULATOR 📟        |")
print("*==============================*\n")

num1 = float(input("➡️ Enter first number: "))
num2 = float(input("➡️ Enter second number: "))

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2

print("\n*==============================*")
print("|          RESULTS ✅          |")
print("*==============================*\n")

print(f"➕ Addition: {addition}")
print(f"➖ Subtraction: {subtraction}")
print(f"✖️ Multiplication: {multiplication}")
print(f"➗ Division: {division:.2f}")  # Here, 2f is used for 2 decimal points

print("\n✨ Thanks for calculating! ✨")



